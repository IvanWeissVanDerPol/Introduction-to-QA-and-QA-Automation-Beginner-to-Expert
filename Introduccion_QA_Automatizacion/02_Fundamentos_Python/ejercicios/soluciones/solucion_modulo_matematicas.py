# Importación del módulo de operaciones matemáticas.
import operaciones_matematicas as om

# Ejemplo de uso de las funciones del módulo.

# Suma de 5 y 3.
print(om.suma(5, 3))  # Output: 8

# Resta de 5 menos 3.
print(om.resta(5, 3))  # Output: 2

# Multiplicación de 5 por 3.
print(om.multiplicacion(5, 3))  # Output: 15

# División de 5 entre 3.
print(om.division(5, 3))  # Output: 1.6666666666666667

# Intento de división por cero.
print(om.division(5, 0))  # Output: División por cero no permitida
