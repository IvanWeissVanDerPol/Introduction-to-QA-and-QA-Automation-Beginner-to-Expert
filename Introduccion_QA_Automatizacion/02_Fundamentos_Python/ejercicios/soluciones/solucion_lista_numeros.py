# Definición de una lista de números.
numeros = [1, 2, 3, 4, 5]

# Suma de los números en la lista.
suma = sum(numeros)
print(f"Suma: {suma}")  # Output: Suma: 15

# Cálculo del promedio de los números en la lista.
# La suma se divide entre la cantidad de elementos en la lista.
promedio = suma / len(numeros)
print(f"Promedio: {promedio}")  # Output: Promedio: 3.0

# Encuentra el número máximo en la lista.
maximo = max(numeros)
print(f"Máximo: {maximo}")  # Output: Máximo: 5
