# Crear una lista de los cuadrados de los números del 1 al 10 usando comprensión de listas.
# Las comprensiones de listas permiten crear listas de manera concisa y eficiente.
cuadrados = [x**2 for x in range(1, 11)]

# Ejemplo de uso.
# Imprime la lista de cuadrados.
print(cuadrados)  # Output: [1, 4, 9, 16, 25, 36, 49, 64, 81, 100]
